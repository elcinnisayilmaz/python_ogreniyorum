#in membership operatörü- bir dizide belirli bir öğenin var olup olmadığını kontrol eder. true veya false olarak.

meyveler=["elma","muz","çilek","portakal"]
print("elma" in meyveler) # True
print("armut" in meyveler) # False

#not in operatörü- bir dizide belirli bir öğenin var olup olmadığını kontrol eder. true veya false olarak.

print("elma" not in meyveler) # False
print("armut" not in meyveler) # True

#<< bitwise operatörü- sağına yazılan sayıyı 2 üssüne çarpar. sonra ise solundaki sayı ile çarpılır.

print(3 << 2) # 3 * 2 üzeri 2 = 12

#>> bitwise operatörü- sağına yazılan sayıyı 2 üssüne çarpar. sonra solundaki sayı ile böler.

print(10 >> 2) # 10/ 2 üzeri 2 = 2.5

